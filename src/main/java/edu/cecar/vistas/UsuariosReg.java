    private void mostrarUsuarios() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        PreparedStatement preparedStatement = getInstance().prepareStatement("SELECT * FROM testdesarrollador.users");
        ResultSet resultSet = preparedStatement.executeQuery();
        ResultSetMetaData rsmd = resultSet.getMetaData();
        int cantidad = rsmd.getColumnCount();
        while (resultSet.next()) {
            Object [] filas = new Object[cantidad];
            for (int i = 0; i < cantidad; i++) {
                filas[i] = resultSet.getObject(i+1);
            }
            modelo.addRow(filas);
        }
    }
    
    
